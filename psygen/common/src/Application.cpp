#include "psygen/Application.hpp"

#include "psygen/Task.hpp"
#include "psygen/Clock.hpp"
#include "psygen/Logger.hpp"


namespace psy { // ============================================================================================================================================



//-------------------------------------------------------------------------------------------------------------------------------------------------------------
Application::Application()
:
    m_is_running(false)
{
    PSY_LOG_DBG("Creating new Application");
}



//-------------------------------------------------------------------------------------------------------------------------------------------------------------
Application::~Application()
{
    PSY_LOG_DBG("Deleting Application");
}



//-------------------------------------------------------------------------------------------------------------------------------------------------------------
void Application::init()
{
    PSY_LOG_DBG("Start Application::init()");

    for (Task* task : m_tasks)
        task->start();

    m_is_running = true;

    PSY_LOG_DBG("Finish Application::init()");
}


//-------------------------------------------------------------------------------------------------------------------------------------------------------------
void Application::shutdown()
{
    PSY_LOG_DBG("Start Application::shutdown()");

    for (Task* task : m_tasks)
        task->stop();

    m_is_running = false;

    PSY_LOG_DBG("Finish Application::shutdown()");
}


//-------------------------------------------------------------------------------------------------------------------------------------------------------------
void Application::run()
{
    PSY_LOG_DBG("    Start Application::run()");

    while (m_is_running)
        for (Task* task : m_tasks)
            task->update();

    PSY_LOG_DBG("    Finish Application::run()");
}


} // end namespace psy ========================================================================================================================================
